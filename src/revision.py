# logical operators


# and


#   True     and    True   -----> True
#   True     and    False   -----> False
#   False     and    True   -----> False
#   False     and    False   -----> False

print(2 == 2 and 3 == 3)
print(2 != 2 and 3 == 3)
print(2 == 2 and 3 != 3)
print(2 != 2 and 3 != 3)

# or

#   True     or    True   -----> True
#   True     or   False   -----> True
#   False    or    True   -----> True
#   False    or    False   -----> False


print(2 == 2 or 3 == 3)
print(2 != 2 or 3 == 3)
print(2 == 2 or 3 != 3)
print(2 != 2 or 3 != 3)



