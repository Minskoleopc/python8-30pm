# logical operator
# and , or , not

# truth table

# and
# True   and    True    =======> True
# False  and    True    =======> False
# True   and    False   ========> False
# False  and    False   ========> False


print(2 == 2  and 3 == 3)
print(2 != 2  and 3 == 3)
print(2 == 2  and 3 != 3)
print(2 != 2  and 3 != 3)

# or
# True   or  True   ======> True
# False  or  True   ======> True
# True   or   False  ======> True
# False  or   False  ======> False

print(4 == 4 or 5 == 5)
print(4 != 4 or 5 == 5)
print(4 == 4 or 5 != 5)
print(4 != 4 or 5 != 5)


# not
# not False  =====> True
# not True   =====> False
print(not 4 == 4)
print(not 8 != 8)

